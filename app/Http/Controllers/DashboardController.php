     * @return \Illuminate\Http\Response
     */
    public function index() {
        $User = User::find(Auth::User()->id);
        if ($User->can('view-any-event')) {
            $events = Event::all();
        } else {
            $events = Event::where('frontpage', true)->get();
        }

        return response()->view('dashboard', [
            'events' => $events
        ]);
    }

